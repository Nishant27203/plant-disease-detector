function getLang() {
  const stored = localStorage.getItem("leafscan_lang");
  return stored === "hi" ? "hi" : "en";
}

function setLang(lang) {
  const code = lang === "hi" ? "hi" : "en";
  localStorage.setItem("leafscan_lang", code);
  document.documentElement.lang = code;
  applyTranslations();
  document.dispatchEvent(new CustomEvent("leafscan:langchange", { detail: { lang: code } }));
}

function t(key) {
  const lang = getLang();
  return I18N[lang]?.[key] ?? I18N.en[key] ?? key;
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  document.querySelectorAll(".lang-switch button").forEach((button) => {
    const active = button.dataset.lang === getLang();
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", active ? "true" : "false");
  });
}

function apiLangQuery() {
  return `lang=${encodeURIComponent(getLang())}`;
}

function initI18n() {
  document.documentElement.lang = getLang();
  applyTranslations();

  document.querySelectorAll(".lang-switch button").forEach((button) => {
    button.addEventListener("click", () => setLang(button.dataset.lang));
  });
}

document.addEventListener("DOMContentLoaded", initI18n);
